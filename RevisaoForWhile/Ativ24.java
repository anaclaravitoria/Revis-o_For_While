public class Ativ24 {
    public static void main(String[] args) {

                int n = 10; // Número de termos desejados
                int soma = 0;

                // Loop para calcular os números triangulares
                for (int i = 1; i <= n; i++) {
                    soma += i; // A cada iteração, soma o valor de i ao total
                    System.out.println("Termo " + i + ": " + soma);
                }
            }
        }

