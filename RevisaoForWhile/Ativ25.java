package RevisaoForWhile;

public class Ativ25 {
    public static void main(String[] args) {
                int altura = 5;

                // Loop para cada linha
                for (int i = 1; i <= altura; i++) {
                    // Imprime os asteriscos para cada linha
                    for (int j = 1; j <= i; j++) {
                        System.out.print("*");
                    }
                    // Pula para a próxima linha após imprimir os asteriscos
                    System.out.println();
                }
            }
        }



